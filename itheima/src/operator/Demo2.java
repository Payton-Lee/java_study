package operator;

public class Demo2 {
    public static void main(String[] args) {
        // 关系运算符
        int a = 10;
        int b = 10;
        boolean rs = a == b;
        System.out.println(rs);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);


        int i = 10;
        int j = 5;

        System.out.println(i == j);
        System.out.println(i != j);
        System.out.println(i > j);
        System.out.println(i >= j);
        System.out.println(i < j);
        System.out.println(i <= j);
        System.out.println(i = j);
    }
}
